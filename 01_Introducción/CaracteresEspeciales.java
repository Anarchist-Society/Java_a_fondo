public class CaracteresEspeciales {
    public static void main(String[] args) {
        String s = "Esto es una cadena \nque sigue en la otra línea";
        System.out.println(s);

        s = "El lenguaje \"Java\" es muy bueno";
        System.out.println(s);

        s = "El carácter \\ es el caracter de escape";
        System.out.println(s);
    }
}
